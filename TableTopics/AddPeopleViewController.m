- (void)alertView:(UIAlertView *)alertView didDismissWithButtonIndex:(NSInteger)buttonIndex
{
    if(buttonIndex == 1){
        NSString *personToAdd = [[alertView textFieldAtIndex:0] text];
        NSLog(@"Persons Name:%@",personToAdd);
        Person *person = (Person *)[NSEntityDescription insertNewObjectForEntityForName:@"Person" inManagedObjectContext:managedObjectContext];
        person.name = personToAdd;
        
        NSError *error;
        
        // here's where the actual save happens, and if it doesn't we print something out to the console
        if (![managedObjectContext save:&error])
        {
            NSLog(@"Problem saving: %@", [error localizedDescription]);
        }
    }
    
}

