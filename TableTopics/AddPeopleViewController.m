-(IBAction)addPerson:(id)sender{
    UIAlertView * alert = [[UIAlertView alloc] initWithTitle:@"Add Person" message:@"Person's Name" delegate:self cancelButtonTitle:@"Cancel" otherButtonTitles:@"Add",nil];
